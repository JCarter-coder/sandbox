"use strict";
function isValidSudoku(board) {
    const N = 9;
    const rows = Array.from({ length: N }, () => Array(N).fill(0));
    const cols = Array.from({ length: N }, () => Array(N).fill(0));
    const boxes = Array.from({ length: N }, () => Array(N).fill(0));
    let result = true;
    for (let row = 0; row < N; row++) {
        for (let col = 0; col < N; col++) {
            if (board[row][col] === ".")
                continue;
            let pos = parseInt(board[row][col]) - 1;
            if (rows[row][pos] === 1) {
                result = false;
                break;
            }
            rows[row][pos] = 1;
            if (cols[col][pos] === 1) {
                result = false;
                break;
            }
            cols[col][pos] = 1;
            let box = Math.floor(row / 3) * 3 + Math.floor(col / 3);
            if (boxes[box][pos] === 1) {
                result = false;
                break;
            }
            boxes[box][pos] = 1;
        }
    }
    console.log(result);
    return result;
}
